package com.setexamples;

public class Bus {
	int bus_id;
	String bus_name;
	int bus_fare;
	String bus_from;
	String bus_to;
	
	
	public Bus(int bus_id, String bus_name, int bus_fare, String bus_from, String bus_to) {
		super();
		this.bus_id = bus_id;
		this.bus_name = bus_name;
		this.bus_fare = bus_fare;
		this.bus_from = bus_from;
		this.bus_to = bus_to;
	}

}
