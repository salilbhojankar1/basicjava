package com.lambdaExample;

public class Cricket {
	String country;
	String captain;
	String best_batsman;
	String best_bowler;
	String coach;
	
	
	public Cricket(String country, String captain, String best_batsman, String best_baller, String coach) {
		
		this.country=country;
		this.captain = captain;
		this.best_batsman = best_batsman;
		this.best_bowler = best_baller;
		this.coach = coach;
	}

}
